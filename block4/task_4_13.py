# Ввод коэффициентов
a = float(input("Введите a (a ≠ 0): "))
b = float(input("Введите b: "))
c = float(input("Введите c: "))

# Проверка, что a не равно 0
if a == 0:
    print("Ошибка: a не может быть равно 0")
else:
    # Вычисление дискриминанта
    d = b ** 2 - 4 * a * c

    # Проверка условия наличия вещественных корней
    if d >= 0:
        print("Уравнение имеет вещественные корни")
    else:
        print("Уравнение не имеет вещественных корней")
