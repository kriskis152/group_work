# Ввод натурального числа
n = int(input("Введите натуральное число: "))

# Проверка, является ли число четным
if n % 2 == 0:
    print(f"а) Число {n} является четным.")
else:
    print(f"а) Число {n} является нечетным.")

# Проверка, оканчивается ли число цифрой 7
# Для этого находим последнюю цифру: остаток от деления на 10
if n % 10 == 7:
    print(f"б) Число {n} оканчивается на цифру 7.")
else:
    print(f"б) Число {n} не оканчивается на цифру 7.")